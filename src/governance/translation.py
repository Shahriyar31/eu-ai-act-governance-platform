def translate(text: str, lang: str = "en") -> str:
    """
    Translates a given string to target language if a matching key is defined.
    Otherwise, gracefully falls back to the original English text.
    """
    if not lang or lang == "en":
        return text
        
    lang = lang.lower()
    if lang not in TRANSLATIONS:
        return text
        
    # Trim to handle dynamic whitespaces gracefully
    stripped_text = text.strip()
    
    # Try exact match first
    if stripped_text in TRANSLATIONS[lang]:
        return TRANSLATIONS[lang][stripped_text]
        
    return text
